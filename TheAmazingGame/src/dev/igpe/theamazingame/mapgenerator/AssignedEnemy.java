package dev.igpe.theamazingame.mapgenerator;

public class AssignedEnemy {

	private int x;
	private int y;
	
	public AssignedEnemy(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
	
}
